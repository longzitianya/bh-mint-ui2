<template>
  <x-cell
    class="mint-field"
    :title="label"
    :titleWidth="titleWidth"
    v-clickoutside="doCloseActive"
    :class="[{
      'is-textarea': type === 'textarea',
      'is-nolabel': !label
    }]">
    <textarea
      @change="$emit('change', currentValue, $event)"
      ref="textarea"
      class="mint-field-core"
      :placeholder="placeholder"
      v-if="type === 'textarea'"
      :rows="rows"
      :disabled="disabled"
      :readonly="readonly"
      v-model="currentValue">
    </textarea>
    <input
      @change="$emit('change', currentValue, $event)"
      ref="input"
      class="mint-field-core"
      :placeholder="placeholder"
      :number="type === 'number'"
      v-else
      :type="type"
      @focus="active = true"
      :disabled="disabled"
      :readonly="readonly"
      :value="currentValue"
      @input="handleInput">
    <div
      @click="handleClear"
      class="mint-field-clear"
      v-if="!disableClear"
      v-show="currentValue && type !== 'textarea' && active">
      <i class="mintui mintui-field-error"></i>
    </div>
    <span class="mint-field-state" v-if="state" :class="['is-' + state]">
      <i class="mintui" :class="['mintui-field-' + state]"></i>
    </span>
    <div class="mint-field-other">
      <slot></slot>
    </div>
  </x-cell>
</template>

<script>
import XCell from 'bh-mint-ui2/packages/cell/index.js';
import Clickoutside from 'bh-mint-ui2/src/utils/clickoutside';
if (process.env.NODE_ENV === 'component') {
  require('bh-mint-ui2/packages/cell/style.css');
}

/**
 * mt-field
 * @desc 编辑器，依赖 cell
 * @module components/field
 *
 * @param {string} [type=text] - field 类型，接受 text, textarea 等
 * @param {string} [label] - 标签
 * @param {string} [rows] - textarea 的 rows
 * @param {string} [placeholder] - placeholder
 * @param {string} [disabled] - disabled
 * @param {string} [readonly] - readonly
 * @param {string} [state] - 表单校验状态样式，接受 error, warning, success
 *
 * @example
 * <mt-field v-model="value" label="用户名"></mt-field>
 * <mt-field v-model="value" label="密码" placeholder="请输入密码"></mt-field>
 * <mt-field v-model="value" label="自我介绍" placeholder="自我介绍" type="textarea" rows="4"></mt-field>
 * <mt-field v-model="value" label="邮箱" placeholder="成功状态" state="success"></mt-field>
 */

/**
 * @noteType component
 * @name Field
 * @tagName mt-field
 * @desc 输入框
 * @wrapClassName mint-field
 * @html
 * <mt-field label="输入框"></mt-field>
 */
export default {
  name: 'mt-field',

  data() {
    return {
      active: false,
      currentValue: this.value
    };
  },

  directives: {
    Clickoutside
  },

  props: {
      /**
       * @noteType prop
       * @field type
       * @desc 输入类型
       * @type select
       * @option
       [
       {
         "text": "文本",
         "value": ""
       },
       {
         "text": "密码",
         "value": "password"
       },
       {
         "text": "日期",
         "value": "date"
       },
       {
         "text": "数字",
         "value": "number"
       },
       {
         "text": "文本域",
         "value": "textarea"
       },
       {
         "text": "邮箱",
         "value": "email"
       },
       {
         "text": "链接",
         "value": "url"
       },
       {
         "text": "电话",
         "value": "tel"
       }
       ]
       */
    type: {
      type: String,
      default: 'text'
    },
    rows: String,
      /**
       * @noteType prop
       * @field label
       * @desc 内容
       * @type input
       * @value 输入框
       */
    label: String,
      /**
       * @noteType prop
       * @field placeholder
       * @desc placeholder
       * @type input
       * @value
       */
    placeholder: String,
      /**
       * @noteType prop
       * @field readonly
       * @desc 是否只读
       * @valueType boolean
       * @type radio
       * @value false
       * @option
       [
       {
           "text": "否",
           "value": false
       },
       {
           "text": "是",
           "value": true
       }
       ]
       */
    readonly: Boolean,
      /**
       * @noteType prop
       * @field disabled
       * @desc 是否禁用
       * @valueType boolean
       * @type radio
       * @value false
       * @option
       [{
           "text": "否",
           "value": false
       },{
           "text": "是",
           "value": true
       }]
       */
    disabled: Boolean,
    disableClear: Boolean,
      /**
       * @noteType prop
       * @field state
       * @desc 校验样式
       * @type select
       * @option
       [
       {
         "text": "默认",
         "value": ""
       },{
         "text": "成功",
         "value": "success"
       },{
         "text": "失败",
         "value": "error"
       },{
         "text": "警告",
         "value": "warning"
       }]
       */
    state: {
      type: String,
      default: 'default'
    },
    /**
     * @noteType prop
     * @field value
     * @desc 默认值
     * @type textarea
      */
    value: {},
    attr: Object,
    titleWidth: {
      type:String,
      default:'20%'
    }
  },

  components: { XCell },

  methods: {
    doCloseActive() {
      this.active = false;
    },

    handleInput(evt) {
      this.currentValue = evt.target.value;
    },

    handleClear() {
      if (this.disabled || this.readonly) return;
      this.currentValue = '';
    }
  },

  watch: {
    value(val) {
      this.currentValue = val;
    },

    currentValue(val) {
      this.$emit('input', val);
    },

    attr: {
      immediate: true,
      handler(attrs) {
        this.$nextTick(() => {
          const target = [this.$refs.input, this.$refs.textarea];
          target.forEach(el => {
            if (!el || !attrs) return;
            Object.keys(attrs).map(name => el.setAttribute(name, attrs[name]));
          });
        });
      }
    }
  }
};
/**
 * @noteType external
 * @content
 {
   "xtype": [ "text", "textarea" ],
   "bindField": "v-model"
 }
 */
</script>

